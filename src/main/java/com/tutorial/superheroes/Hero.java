package com.tutorial.superheroes;

/**
 * Clase con las características de un héroe
 * @author superman
 */
public class Hero {
	
	private final String name;

	/**
	 * Constructor
	 * @param name
	 */
	public Hero(String name) {
		super();
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
}
